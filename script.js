const navToggle = document.querySelector("[data-nav-toggle]");
const nav = document.querySelector("[data-nav]");
const header = document.querySelector("[data-header]");
const slides = Array.from(document.querySelectorAll(".hero-image"));
const inquiryForm = document.querySelector("[data-inquiry-form]");
const languageButtons = Array.from(document.querySelectorAll("[data-lang]"));

const getText = (key, lang) => translations[lang]?.[key] || translations.zh[key] || "";

const applyLanguage = (lang) => {
  const selected = translations[lang] ? lang : "zh";
  document.documentElement.lang = langMeta[selected].htmlLang;
  document.title = langMeta[selected].title;

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const value = getText(element.dataset.i18n, selected);
    if (value) element.textContent = value;
  });

  document.querySelectorAll("[data-i18n-attr]").forEach((element) => {
    element.dataset.i18nAttr.split(",").forEach((entry) => {
      const [attribute, key] = entry.split(":");
      const value = getText(key, selected);
      if (attribute && value) element.setAttribute(attribute, value);
    });
  });

  languageButtons.forEach((button) => {
    button.classList.toggle("is-active", button.dataset.lang === selected);
  });

  localStorage.setItem("gw-site-lang", selected);
};

if (languageButtons.length) {
  const savedLanguage = localStorage.getItem("gw-site-lang") || "zh";
  applyLanguage(savedLanguage);

  languageButtons.forEach((button) => {
    button.addEventListener("click", () => applyLanguage(button.dataset.lang));
  });
}

if (navToggle && nav) {
  navToggle.addEventListener("click", () => {
    const isOpen = nav.classList.toggle("is-open");
    navToggle.setAttribute("aria-expanded", String(isOpen));
    document.body.classList.toggle("nav-open", isOpen);
  });

  nav.addEventListener("click", (event) => {
    if (event.target instanceof HTMLAnchorElement) {
      nav.classList.remove("is-open");
      navToggle.setAttribute("aria-expanded", "false");
      document.body.classList.remove("nav-open");
    }
  });
}

if (header) {
  const updateHeader = () => {
    header.classList.toggle("is-scrolled", window.scrollY > 8);
  };

  updateHeader();
  window.addEventListener("scroll", updateHeader, { passive: true });
}

if (slides.length > 1) {
  let current = 0;

  window.setInterval(() => {
    slides[current].classList.remove("is-active");
    current = (current + 1) % slides.length;
    slides[current].classList.add("is-active");
  }, 5200);
}

